//do while
let i = 0
do {
    i++
    console.log(i)
} while (i < 2)

// for
let j = 0
for (j; j < 2; j++) {
    console.log('----->j<-----', j)
}

// переберемо масив через do while
const arr = [1, 2, 3]

let k = 0
do {
    console.log('----->arr[k]<-----', arr[k])
    k++
} while (k < 3)

